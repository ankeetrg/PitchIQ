function impliedProbability(odds: string): string {
  const value = Number(odds.replace("+", ""));
  if (Number.isNaN(value)) return "N/A";
  const probability = value < 0 ? Math.abs(value) / (Math.abs(value) + 100) : 100 / (value + 100);
  return `${Math.round(probability * 100)}%`;
}

