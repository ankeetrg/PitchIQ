export function MatchOddsSection({ match }: { match: Match }) {
  const rows = [
    { label: `${match.home} win`, odds: match.homeMl, implied: impliedProbability(match.homeMl) },
    { label: "Draw", odds: match.drawMl, implied: impliedProbability(match.drawMl) },
    { label: `${match.away} win`, odds: match.awayMl, implied: impliedProbability(match.awayMl) },
    { label: `Over ${match.ouLine}`, odds: match.ouOver, implied: impliedProbability(match.ouOver) },
    { label: `Under ${match.ouLine}`, odds: match.ouUnder, implied: impliedProbability(match.ouUnder) },
  ];

  return (
    <section className="content-card odds-section">
      <div className="section-heading">
        <h2>Odds and Market Read</h2>
        <span className="ai-content-badge">AI Pick</span>
      </div>
      <div className="odds-table">
        {rows.map((row) => (
          <div className="odds-row" key={row.label}>
            <span>{row.label}</span>
            <strong>{row.odds}</strong>
            <em>{row.implied} implied</em>
          </div>
        ))}
      </div>
      <p>
        {match.favName} are the market side. The model&rsquo;s job is simple: compare that line to the
        win probability and skip prices that are already gone.
      </p>
      <AffiliateOddsSlot />
      <OddsDisclaimer />
    </section>
  );
}

